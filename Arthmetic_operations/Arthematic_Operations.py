#----------------------------------------#
#---------- Arthmatic Oprations ---------#
#----------------------------------------#


#----------------------------------#
#---------- Operation {+} ---------#
#----------------------------------#
x  = 20 + 30 	     # 20 + 30 = 50
print("=> 20 + 30 = ",x)

#----------------------------------#
#---------- Operation {-} ---------#
#----------------------------------#
x  = 30 - 10 	     # 30 - 10 = 20
print("=> 30 - 10 = ",x)

#----------------------------------#
#---------- Operation {*} ---------#
#----------------------------------#
x  = 30 * 10 	     # 30 * 10 = 300
print("=> 30 * 10 = ",x)

#----------------------------------#
#---------- Operation {/} ---------#
#----------------------------------#
x  = 30 / 10 	     # 30 / 10 = 3.0
print("=> 30 / 10 = ",x)

#----------------------------------#
#--------- Operation {**} ---------#
#----------------------------------#
x  = 4 ** 2 	     # 4 ** 2 = 16
		     # ** => power
print("=> 4 ** 2  = ",x)

#----------------------------------#
#--------- Operation {%} ----------#
#----------------------------------#
x  = 4 % 3 	     # 4 % 3 = 1
		     # % => Rest of the division
print("=> 4 % 3   = ",x)
