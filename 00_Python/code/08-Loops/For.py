#!/bin/python3

#----------------------------------------#
#----- For Loop With Rang Function ------#
#----------------------------------------#
print("----- For Loop With Rang Function -----")
for Num in range(10):
	print("Number [",Num,"]")

#----------------------------------#
#----- For Loop With String ------#
#----------------------------------#
print("----- For Loop With String -----")
print("String is {mohammed}.")
for char in "mohammed":
	print("char [",char,"]")

#----------------------------------#
#----- For Loop With List ------#
#----------------------------------#
print("----- For Loop With List -----")
list1 = ["ziad","mohammed","fathi","omar","adham"]
for string1 in list1:
	print("item [",string1,"]")
